    #[test]
    fn canonical_name_ordering_follows_rfc4034() {
        use std::cmp::Ordering;
        assert_eq!(
            canonical_name_cmp("example.com", "example.com"),
            Ordering::Equal
        );
        // Shorter name (parent) sorts before its child.
        assert_eq!(
            canonical_name_cmp("example.com", "a.example.com"),
            Ordering::Less
        );
        // Rightmost label dominates.
        assert_eq!(
            canonical_name_cmp("z.example.com", "a.example.org"),
            Ordering::Less
        );
        // Case-insensitive.
        assert_eq!(
            canonical_name_cmp("EXAMPLE.com", "example.com"),
            Ordering::Equal
        );
    }

