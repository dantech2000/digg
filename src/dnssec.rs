//! Local DNSSEC chain-of-trust validation (+validate).
//!
//! Verifies RRSIGs, DS digests, and the chain from the answer's signing
//! zone up to the IANA root trust anchors — without trusting the
//! resolver's AD bit. RFC 4034/4035 canonical form, RFC 6840 clarifications.

use crate::error::DnsError;
use crate::protocol::edns::EdnsOptions;
use crate::protocol::message::DnsMessage;
use crate::protocol::name::encode_name;
use crate::protocol::record::{RData, ResourceRecord};
use crate::protocol::types::{RecordClass, RecordType};
use crate::transport;
use aws_lc_rs::{digest, signature};
use std::time::Duration;

/// IANA root zone trust anchors (DS form, SHA-256).
struct TrustAnchor {
    key_tag: u16,
    digest: &'static [u8],
}

const ROOT_ANCHORS: &[TrustAnchor] = &[
    // KSK-2017 (still published)
    TrustAnchor {
        key_tag: 20326,
        digest: &[
            0xE0, 0x6D, 0x44, 0xB8, 0x0B, 0x8F, 0x1D, 0x39, 0xA9, 0x5C, 0x0B, 0x0D, 0x7C, 0x65,
            0xD0, 0x84, 0x58, 0xE8, 0x80, 0x40, 0x9B, 0xBC, 0x68, 0x34, 0x57, 0x10, 0x42, 0x37,
            0xC7, 0xF8, 0xEC, 0x8D,
        ],
    },
    // KSK-2024
    TrustAnchor {
        key_tag: 38696,
        digest: &[
            0x68, 0x3D, 0x2D, 0x0A, 0xCB, 0x8C, 0x9B, 0x71, 0x2A, 0x19, 0x48, 0xB2, 0x7F, 0x74,
            0x12, 0x19, 0x29, 0x8D, 0x0A, 0x45, 0x0D, 0x61, 0x2C, 0x48, 0x3A, 0xF4, 0x44, 0xA4,
            0xC0, 0xFB, 0x2B, 0x16,
        ],
    },
];

#[derive(Debug, PartialEq)]
pub enum ChainStatus {
    /// Every link verified down from a root trust anchor.
    Secure,
    /// The chain legitimately ends: an unsigned delegation (no DS).
    Insecure(String),
    /// A signature, digest, or timing check failed.
    Bogus(String),
}

pub struct LinkReport {
    pub zone: String,
    pub detail: String,
    pub ok: bool,
}

pub struct ValidationReport {
    pub links: Vec<LinkReport>,
    pub status: ChainStatus,
}

/// Validate the answer's chain of trust by issuing our own DNSKEY/DS
/// queries through the given resolver and verifying everything locally.
#[allow(clippy::too_many_arguments)]
pub fn validate(
    server: &str,
    port: u16,
    answer: &DnsMessage,
    qname: &str,
    qtype: RecordType,
    timeout: Duration,
    cd: bool,
) -> Result<ValidationReport, DnsError> {
    let now = std::time::SystemTime::now()
        .duration_since(std::time::UNIX_EPOCH)
        .map(|d| d.as_secs() as u32)
        .unwrap_or(0);
    let fetch = |name: &str, rtype: RecordType| -> Result<DnsMessage, DnsError> {
        let edns = EdnsOptions {
            dnssec_ok: true,
            ..EdnsOptions::default()
        };
        let (query, id) = DnsMessage::build_query_with_class(
            name,
            rtype,
            RecordClass::IN,
            true,
            cd,
            Some(&edns),
        )?;
        let result = transport::send_query_with_retries(server, port, &query, false, timeout, 2)?;
        transport::verify_id(&result.message.header, id)?;
        Ok(result.message)
    };
    validate_with(answer, qname, qtype, now, &fetch)
}

/// Core validation, parameterized over time and record fetching so tests
/// can run hermetically against captured fixtures.
pub fn validate_with(
    answer: &DnsMessage,
    qname: &str,
    qtype: RecordType,
    now: u32,
    fetch: &dyn Fn(&str, RecordType) -> Result<DnsMessage, DnsError>,
) -> Result<ValidationReport, DnsError> {
    let mut links = Vec::new();

    // 1. The answer RRset and its covering RRSIG.
    let rrset: Vec<&ResourceRecord> = answer
        .answers
        .iter()
        .filter(|rr| rr.rtype == qtype)
        .collect();
    let rrsigs: Vec<&ResourceRecord> = answer
        .answers
        .iter()
        .filter(
            |rr| matches!(&rr.rdata, RData::RRSIG { type_covered, .. } if *type_covered == qtype),
        )
        .collect();

    // Empty answer of the requested type: this is a negative response
    // (NXDOMAIN or NODATA). Validate the denial-of-existence proof carried
    // in the authority section, which establishes the signing zone; then
    // fall through to the shared chain walk to the root.
    if rrset.is_empty() {
        match validate_denial(answer, qname, qtype, now, fetch)? {
            DenialOutcome::Proven {
                signer,
                mut denial_links,
            } => {
                links.append(&mut denial_links);
                return chain_to_root(signer, now, fetch, links);
            }
            DenialOutcome::Unsigned(why) => {
                return Ok(ValidationReport {
                    links,
                    status: ChainStatus::Insecure(why),
                });
            }
            DenialOutcome::Bogus {
                mut denial_links,
                reason,
            } => {
                links.append(&mut denial_links);
                return Ok(ValidationReport {
                    links,
                    status: ChainStatus::Bogus(reason),
                });
            }
        }
    }

    let Some(rrsig) = rrsigs.first() else {
        // Unsigned answer: walk ancestors to find whether that is legitimate.
        let status = classify_unsigned(qname, fetch);
        return Ok(ValidationReport { links, status });
    };

    let RData::RRSIG { signer, .. } = &rrsig.rdata else {
        unreachable!("filtered on RRSIG above");
    };
    let zone = signer.clone();

    // 2. Verify the answer RRset against the signer zone's DNSKEY set.
    let dnskey_msg = fetch(&zone, RecordType::DNSKEY)?;
    let dnskeys: Vec<&ResourceRecord> = dnskey_msg
        .answers
        .iter()
        .filter(|rr| rr.rtype == RecordType::DNSKEY)
        .collect();
    match verify_rrsig(&rrset, rrsig, &dnskeys, now) {
        Ok(tag) => links.push(LinkReport {
            zone: zone.clone(),
            detail: format!("{} RRSIG verified (key {})", qtype, tag),
            ok: true,
        }),
        Err(e) => {
            links.push(LinkReport {
                zone: zone.clone(),
                detail: format!("{} RRSIG failed: {}", qtype, e),
                ok: false,
            });
            return Ok(ValidationReport {
                links,
                status: ChainStatus::Bogus(format!("{}: answer signature invalid: {}", zone, e)),
            });
        }
    }

    // 3. Walk the chain from the signing zone to the root anchors.
    chain_to_root(zone, now, fetch, links)
}

/// Walk from `zone` up to the root, verifying each zone's DNSKEY RRset and
/// matching it against the parent's DS, until a root trust anchor. Shared by
/// positive-answer and denial-of-existence validation.
fn chain_to_root(
    mut zone: String,
    now: u32,
    fetch: &dyn Fn(&str, RecordType) -> Result<DnsMessage, DnsError>,
    mut links: Vec<LinkReport>,
) -> Result<ValidationReport, DnsError> {
    loop {
        let dnskey_msg = fetch(&zone, RecordType::DNSKEY)?;
        let dnskeys: Vec<&ResourceRecord> = dnskey_msg
            .answers
            .iter()
            .filter(|rr| rr.rtype == RecordType::DNSKEY)
            .collect();
        let dnskey_sigs: Vec<&ResourceRecord> = dnskey_msg
            .answers
            .iter()
            .filter(|rr| {
                matches!(&rr.rdata, RData::RRSIG { type_covered, .. } if *type_covered == RecordType::DNSKEY)
            })
            .collect();

        // 3a. DNSKEY RRset must be self-consistent (signed by a key in it).
        let self_sig = dnskey_sigs
            .iter()
            .find_map(|sig| verify_rrsig(&dnskeys, sig, &dnskeys, now).ok());
        match self_sig {
            Some(tag) => links.push(LinkReport {
                zone: zone.clone(),
                detail: format!("DNSKEY RRset verified (KSK {})", tag),
                ok: true,
            }),
            None => {
                let detail = if dnskey_sigs.is_empty() {
                    "DNSKEY RRset has no RRSIG".to_string()
                } else {
                    "DNSKEY RRset signature invalid".to_string()
                };
                links.push(LinkReport {
                    zone: zone.clone(),
                    detail: detail.clone(),
                    ok: false,
                });
                return Ok(ValidationReport {
                    links,
                    status: ChainStatus::Bogus(format!("{}: {}", zone, detail)),
                });
            }
        }

        if zone == "." {
            // 3b. Root: DNSKEY KSK must match a built-in trust anchor.
            let matched = dnskeys.iter().any(|key| {
                ROOT_ANCHORS.iter().any(|anchor| {
                    key_tag(&key.raw_rdata) == anchor.key_tag
                        && ds_digest(".", key, 2).as_deref() == Some(anchor.digest)
                })
            });
            if matched {
                links.push(LinkReport {
                    zone: zone.clone(),
                    detail: "root KSK matches IANA trust anchor".to_string(),
                    ok: true,
                });
                return Ok(ValidationReport {
                    links,
                    status: ChainStatus::Secure,
                });
            }
            links.push(LinkReport {
                zone: zone.clone(),
                detail: "root DNSKEY does not match any built-in trust anchor".to_string(),
                ok: false,
            });
            return Ok(ValidationReport {
                links,
                status: ChainStatus::Bogus("root trust anchor mismatch".to_string()),
            });
        }

        // 3c. Parent DS must match one of this zone's keys.
        let ds_msg = fetch(&zone, RecordType::DS)?;
        let ds_set: Vec<&ResourceRecord> = ds_msg
            .answers
            .iter()
            .filter(|rr| rr.rtype == RecordType::DS)
            .collect();
        if ds_set.is_empty() {
            links.push(LinkReport {
                zone: zone.clone(),
                detail: "no DS at parent (unsigned delegation)".to_string(),
                ok: true,
            });
            return Ok(ValidationReport {
                links,
                status: ChainStatus::Insecure(format!("{}: unsigned delegation, chain ends", zone)),
            });
        }

        let ds_match = ds_set.iter().find_map(|ds| {
            let RData::DS {
                key_tag: tag,
                digest_type,
                digest,
                ..
            } = &ds.rdata
            else {
                return None;
            };
            dnskeys.iter().find_map(|key| {
                if key_tag(&key.raw_rdata) == *tag
                    && ds_digest(&zone, key, *digest_type).as_deref() == Some(digest.as_slice())
                {
                    Some(*tag)
                } else {
                    None
                }
            })
        });
        match ds_match {
            Some(tag) => links.push(LinkReport {
                zone: zone.clone(),
                detail: format!("DS\u{2192}DNSKEY digest verified (key {})", tag),
                ok: true,
            }),
            None => {
                links.push(LinkReport {
                    zone: zone.clone(),
                    detail: "no DS digest matches any DNSKEY".to_string(),
                    ok: false,
                });
                return Ok(ValidationReport {
                    links,
                    status: ChainStatus::Bogus(format!("{}: DS/DNSKEY digest mismatch", zone)),
                });
            }
        }

        // 3d. The DS RRset itself is signed by the parent zone.
        let parent = ds_msg
            .answers
            .iter()
            .find_map(|rr| match &rr.rdata {
                RData::RRSIG {
                    type_covered,
                    signer,
                    ..
                } if *type_covered == RecordType::DS => Some(signer.clone()),
                _ => None,
            })
            .unwrap_or_else(|| parent_zone(&zone));
        let ds_sig = ds_msg.answers.iter().find(|rr| {
            matches!(&rr.rdata, RData::RRSIG { type_covered, .. } if *type_covered == RecordType::DS)
        });
        if let Some(sig) = ds_sig {
            let parent_keys_msg = fetch(&parent, RecordType::DNSKEY)?;
            let parent_keys: Vec<&ResourceRecord> = parent_keys_msg
                .answers
                .iter()
                .filter(|rr| rr.rtype == RecordType::DNSKEY)
                .collect();
            match verify_rrsig(&ds_set, sig, &parent_keys, now) {
                Ok(tag) => links.push(LinkReport {
                    zone: zone.clone(),
                    detail: format!("DS RRSIG verified by {} (key {})", parent, tag),
                    ok: true,
                }),
                Err(e) => {
                    links.push(LinkReport {
                        zone: zone.clone(),
                        detail: format!("DS RRSIG failed: {}", e),
                        ok: false,
                    });
                    return Ok(ValidationReport {
                        links,
                        status: ChainStatus::Bogus(format!(
                            "{}: DS signature invalid: {}",
                            zone, e
                        )),
                    });
                }
            }
        }

        zone = parent;
    }
}

/// For an unsigned answer, decide Insecure vs Bogus by looking for the
/// nearest unsigned delegation above the query name.
fn classify_unsigned(
    qname: &str,
    fetch: &dyn Fn(&str, RecordType) -> Result<DnsMessage, DnsError>,
) -> ChainStatus {
    let mut zone = qname.trim_end_matches('.').to_string();
    loop {
        let probe = format!("{}.", zone);
        match fetch(&probe, RecordType::DS) {
            Ok(msg) => {
                let has_ds = msg.answers.iter().any(|rr| rr.rtype == RecordType::DS);
                if !has_ds {
                    return ChainStatus::Insecure(format!(
                        "{} has no DS (unsigned zone); answer cannot be validated",
                        probe
                    ));
                }
            }
            Err(e) => {
                return ChainStatus::Insecure(format!("could not probe DS for {}: {}", probe, e))
            }
        }
        match zone.split_once('.') {
            Some((_, rest)) => zone = rest.to_string(),
            None => {
                // Every cut up to the TLD has a DS, yet the answer is unsigned.
                return ChainStatus::Bogus(
                    "zone chain is signed but the answer carries no RRSIG".to_string(),
                );
            }
        }
    }
}

fn parent_zone(zone: &str) -> String {
    match zone.trim_end_matches('.').split_once('.') {
        Some((_, rest)) => format!("{}.", rest),
        None => ".".to_string(),
    }
}

/// RFC 4034 Appendix B key tag over the raw DNSKEY RDATA.
pub fn key_tag(dnskey_rdata: &[u8]) -> u16 {
    let mut acc: u32 = 0;
    for (i, &b) in dnskey_rdata.iter().enumerate() {
        if i % 2 == 0 {
            acc += (b as u32) << 8;
        } else {
            acc += b as u32;
        }
    }
    acc += (acc >> 16) & 0xFFFF;
    (acc & 0xFFFF) as u16
}

/// DS digest of owner || DNSKEY RDATA. digest_type: 1=SHA-1, 2=SHA-256, 4=SHA-384.
fn ds_digest(owner: &str, dnskey: &ResourceRecord, digest_type: u8) -> Option<Vec<u8>> {
    let algorithm = match digest_type {
        1 => &digest::SHA1_FOR_LEGACY_USE_ONLY,
        2 => &digest::SHA256,
        4 => &digest::SHA384,
        _ => return None,
    };
    let mut data = canonical_name(owner).ok()?;
    data.extend_from_slice(&dnskey.raw_rdata);
    Some(digest::digest(algorithm, &data).as_ref().to_vec())
}

fn canonical_name(name: &str) -> Result<Vec<u8>, DnsError> {
    encode_name(&name.to_lowercase())
}

/// Canonical RDATA per RFC 4034 §6.2: embedded names in the listed types
/// are lowercased and uncompressed (re-encoded from the parsed form);
/// everything else is the raw wire bytes.
fn canonical_rdata(rr: &ResourceRecord) -> Result<Vec<u8>, DnsError> {
    match &rr.rdata {
        RData::NS(name) | RData::CNAME(name) | RData::PTR(name) => canonical_name(name),
        RData::MX {
            preference,
            exchange,
        } => {
            let mut buf = preference.to_be_bytes().to_vec();
            buf.extend(canonical_name(exchange)?);
            Ok(buf)
        }
        RData::SRV {
            priority,
            weight,
            port,
            target,
        } => {
            let mut buf = Vec::new();
            buf.extend_from_slice(&priority.to_be_bytes());
            buf.extend_from_slice(&weight.to_be_bytes());
            buf.extend_from_slice(&port.to_be_bytes());
            buf.extend(canonical_name(target)?);
            Ok(buf)
        }
        RData::SOA {
            mname,
            rname,
            serial,
            refresh,
            retry,
            expire,
            minimum,
        } => {
            let mut buf = canonical_name(mname)?;
            buf.extend(canonical_name(rname)?);
            for v in [serial, refresh, retry, expire, minimum] {
                buf.extend_from_slice(&v.to_be_bytes());
            }
            Ok(buf)
        }
        _ => Ok(rr.raw_rdata.clone()),
    }
}

/// Verify one RRSIG over an RRset with a DNSKEY set. Returns the key tag
/// of the validating key.
pub fn verify_rrsig(
    rrset: &[&ResourceRecord],
    rrsig: &ResourceRecord,
    dnskeys: &[&ResourceRecord],
    now: u32,
) -> Result<u16, String> {
    let RData::RRSIG {
        type_covered,
        algorithm,
        labels,
        original_ttl,
        expiration,
        inception,
        key_tag: sig_tag,
        signer,
        signature,
    } = &rrsig.rdata
    else {
        return Err("not an RRSIG record".to_string());
    };

    // RFC 1982 serial-number comparison keeps this correct across the
    // 2038/2106 wrap; a plain range check is fine for current dates.
    if now < *inception {
        return Err(format!("signature not yet valid (inception {})", inception));
    }
    if now > *expiration {
        return Err(format!("signature expired at {}", expiration));
    }

    let signed = build_signed_data(
        *type_covered,
        *algorithm,
        *labels,
        *original_ttl,
        *expiration,
        *inception,
        *sig_tag,
        signer,
        rrset,
    )
    .map_err(|e| e.to_string())?;

    let key = dnskeys
        .iter()
        .find(|k| {
            key_tag(&k.raw_rdata) == *sig_tag
                && matches!(&k.rdata, RData::DNSKEY { algorithm: a, .. } if a == algorithm)
        })
        .ok_or_else(|| format!("no DNSKEY with tag {} algorithm {}", sig_tag, algorithm))?;
    let RData::DNSKEY { public_key, .. } = &key.rdata else {
        return Err("matched record is not a DNSKEY".to_string());
    };

    verify_signature(*algorithm, public_key, &signed, signature)?;
    Ok(*sig_tag)
}

#[allow(clippy::too_many_arguments)]
fn build_signed_data(
    type_covered: RecordType,
    algorithm: u8,
    labels: u8,
    original_ttl: u32,
    expiration: u32,
    inception: u32,
    key_tag: u16,
    signer: &str,
    rrset: &[&ResourceRecord],
) -> Result<Vec<u8>, DnsError> {
    // RRSIG RDATA with the signature field removed.
    let mut data = Vec::new();
    data.extend_from_slice(&type_covered.to_u16().to_be_bytes());
    data.push(algorithm);
    data.push(labels);
    data.extend_from_slice(&original_ttl.to_be_bytes());
    data.extend_from_slice(&expiration.to_be_bytes());
    data.extend_from_slice(&inception.to_be_bytes());
    data.extend_from_slice(&key_tag.to_be_bytes());
    data.extend(canonical_name(signer)?);

    // Canonical RRset: sorted by canonical RDATA, owner lowercased and
    // wildcard-adjusted to the RRSIG label count, TTL = original TTL.
    let mut entries: Vec<(Vec<u8>, &&ResourceRecord)> = rrset
        .iter()
        .map(|rr| canonical_rdata(rr).map(|c| (c, rr)))
        .collect::<Result<_, _>>()?;
    entries.sort_by(|a, b| a.0.cmp(&b.0));
    entries.dedup_by(|a, b| a.0 == b.0);

    for (canon, rr) in entries {
        let owner = wildcard_owner(&rr.name, labels);
        data.extend(canonical_name(&owner)?);
        data.extend_from_slice(&rr.rtype.to_u16().to_be_bytes());
        data.extend_from_slice(&rr.rclass.to_u16().to_be_bytes());
        data.extend_from_slice(&original_ttl.to_be_bytes());
        data.extend_from_slice(&(canon.len() as u16).to_be_bytes());
        data.extend_from_slice(&canon);
    }
    Ok(data)
}

/// RFC 4035 §5.3.2: when the RRSIG label count is smaller than the owner's,
/// the RRset was synthesized from a wildcard — validate against "*.<suffix>".
fn wildcard_owner(name: &str, labels: u8) -> String {
    let trimmed = name.trim_end_matches('.');
    if trimmed.is_empty() {
        // Root owner: zero labels by definition, never wildcard-adjusted.
        return ".".to_string();
    }
    let parts: Vec<&str> = trimmed.split('.').collect();
    if parts.len() > labels as usize {
        let suffix = &parts[parts.len() - labels as usize..];
        format!("*.{}.", suffix.join("."))
    } else {
        name.to_string()
    }
}

fn verify_signature(
    algorithm: u8,
    public_key: &[u8],
    signed: &[u8],
    sig: &[u8],
) -> Result<(), String> {
    match algorithm {
        8 | 10 => {
            // RSA/SHA-256 (8) or RSA/SHA-512 (10): DNSKEY holds explen/exp/mod.
            let der = rsa_dnskey_to_der(public_key).ok_or("malformed RSA DNSKEY")?;
            let alg: &dyn signature::VerificationAlgorithm = if algorithm == 8 {
                &signature::RSA_PKCS1_1024_8192_SHA256_FOR_LEGACY_USE_ONLY
            } else {
                &signature::RSA_PKCS1_2048_8192_SHA512
            };
            signature::UnparsedPublicKey::new(alg, &der)
                .verify(signed, sig)
                .map_err(|_| "RSA signature verification failed".to_string())
        }
        13 | 14 => {
            // ECDSA P-256/SHA-256 or P-384/SHA-384: key is raw x||y.
            let mut sec1 = vec![0x04];
            sec1.extend_from_slice(public_key);
            let alg: &dyn signature::VerificationAlgorithm = if algorithm == 13 {
                &signature::ECDSA_P256_SHA256_FIXED
            } else {
                &signature::ECDSA_P384_SHA384_FIXED
            };
            signature::UnparsedPublicKey::new(alg, &sec1)
                .verify(signed, sig)
                .map_err(|_| "ECDSA signature verification failed".to_string())
        }
        15 => signature::UnparsedPublicKey::new(&signature::ED25519, public_key)
            .verify(signed, sig)
            .map_err(|_| "Ed25519 signature verification failed".to_string()),
        other => Err(format!("unsupported DNSSEC algorithm {}", other)),
    }
}

/// Convert the DNSKEY RSA wire format (RFC 3110: explen, exponent, modulus)
/// to a DER RSAPublicKey (SEQUENCE { modulus, exponent }).
fn rsa_dnskey_to_der(key: &[u8]) -> Option<Vec<u8>> {
    if key.is_empty() {
        return None;
    }
    let (exp, modulus) = if key[0] != 0 {
        let len = key[0] as usize;
        (key.get(1..1 + len)?, key.get(1 + len..)?)
    } else {
        let len = u16::from_be_bytes([*key.get(1)?, *key.get(2)?]) as usize;
        (key.get(3..3 + len)?, key.get(3 + len..)?)
    };

    fn der_integer(bytes: &[u8]) -> Vec<u8> {
        let mut body = bytes.to_vec();
        if body.first().is_some_and(|b| b & 0x80 != 0) {
            body.insert(0, 0);
        }
        let mut out = vec![0x02];
        out.extend(der_len(body.len()));
        out.extend(body);
        out
    }
    fn der_len(len: usize) -> Vec<u8> {
        if len < 128 {
            vec![len as u8]
        } else {
            let bytes: Vec<u8> = len
                .to_be_bytes()
                .iter()
                .copied()
                .skip_while(|&b| b == 0)
                .collect();
            let mut out = vec![0x80 | bytes.len() as u8];
            out.extend(bytes);
            out
        }
    }

    let mut body = der_integer(modulus);
    body.extend(der_integer(exp));
    let mut der = vec![0x30];
    der.extend(der_len(body.len()));
    der.extend(body);
    Some(der)
}

// === Denial of existence (RFC 4035 §5.4, RFC 5155 §8) ===

enum DenialOutcome {
    /// The negative answer is cryptographically proven; `signer` is the zone
    /// whose DNSKEY signed the NSEC/NSEC3 records (chain-walked to root next).
    Proven {
        signer: String,
        denial_links: Vec<LinkReport>,
    },
    /// No signed denial records present — an unsigned zone, legitimately.
    Unsigned(String),
    /// Denial records present but a signature failed or the proof does not
    /// cover the query. Never let this fall through to Secure.
    Bogus {
        denial_links: Vec<LinkReport>,
        reason: String,
    },
}

fn validate_denial(
    answer: &DnsMessage,
    qname: &str,
    qtype: RecordType,
    now: u32,
    fetch: &dyn Fn(&str, RecordType) -> Result<DnsMessage, DnsError>,
) -> Result<DenialOutcome, DnsError> {
    let nsec_recs: Vec<&ResourceRecord> = answer
        .authority
        .iter()
        .filter(|rr| rr.rtype == RecordType::NSEC)
        .collect();
    let nsec3_recs: Vec<&ResourceRecord> = answer
        .authority
        .iter()
        .filter(|rr| rr.rtype == RecordType::NSEC3)
        .collect();

    if nsec_recs.is_empty() && nsec3_recs.is_empty() {
        return Ok(DenialOutcome::Unsigned(format!(
            "negative answer for {} carries no NSEC/NSEC3 records (unsigned zone)",
            qname
        )));
    }

    // The signer of the NSEC/NSEC3 RRSIGs is the authoritative zone.
    let denial_type = if !nsec3_recs.is_empty() {
        RecordType::NSEC3
    } else {
        RecordType::NSEC
    };
    let signer = answer.authority.iter().find_map(|rr| match &rr.rdata {
        RData::RRSIG {
            type_covered,
            signer,
            ..
        } if *type_covered == denial_type => Some(signer.clone()),
        _ => None,
    });
    let Some(signer) = signer else {
        return Ok(DenialOutcome::Bogus {
            denial_links: Vec::new(),
            reason: format!("{} records present but unsigned", denial_type),
        });
    };

    // Fetch the signer's DNSKEY and verify every denial RRSIG (NSEC/NSEC3 and
    // the SOA) against it. Group records by owner+type so each RRset verifies.
    let dnskey_msg = fetch(&signer, RecordType::DNSKEY)?;
    let dnskeys: Vec<&ResourceRecord> = dnskey_msg
        .answers
        .iter()
        .filter(|rr| rr.rtype == RecordType::DNSKEY)
        .collect();

    let mut denial_links = Vec::new();
    if let Err(reason) = verify_denial_signatures(&answer.authority, &dnskeys, now) {
        denial_links.push(LinkReport {
            zone: signer.clone(),
            detail: format!("denial RRSIG failed: {}", reason),
            ok: false,
        });
        return Ok(DenialOutcome::Bogus {
            denial_links,
            reason: format!("{}: denial signature invalid: {}", signer, reason),
        });
    }

    // Signatures are valid; now the cover proof.
    let proven = if !nsec3_recs.is_empty() {
        prove_nsec3(&nsec3_recs, qname, &signer, qtype)
    } else {
        prove_nsec(&nsec_recs, qname, qtype)
    };

    match proven {
        Ok(detail) => {
            denial_links.push(LinkReport {
                zone: signer.clone(),
                detail,
                ok: true,
            });
            Ok(DenialOutcome::Proven {
                signer,
                denial_links,
            })
        }
        Err(reason) => {
            denial_links.push(LinkReport {
                zone: signer.clone(),
                detail: format!("denial not proven: {}", reason),
                ok: false,
            });
            Ok(DenialOutcome::Bogus {
                denial_links,
                reason: format!("{}: {}", signer, reason),
            })
        }
    }
}

/// Verify the RRSIG over every NSEC/NSEC3/SOA RRset in the authority section.
fn verify_denial_signatures(
    authority: &[ResourceRecord],
    dnskeys: &[&ResourceRecord],
    now: u32,
) -> Result<(), String> {
    let mut verified_any = false;
    for rr in authority {
        let RData::RRSIG { type_covered, .. } = &rr.rdata else {
            continue;
        };
        // The RRset this RRSIG covers: same owner name and covered type.
        let covered: Vec<&ResourceRecord> = authority
            .iter()
            .filter(|c| c.rtype == *type_covered && names_equal(&c.name, &rr.name))
            .collect();
        if covered.is_empty() {
            continue;
        }
        verify_rrsig(&covered, rr, dnskeys, now)?;
        verified_any = true;
    }
    if verified_any {
        Ok(())
    } else {
        Err("no denial RRSIG could be verified".to_string())
    }
}

/// Compare two domain names ignoring case and any trailing root dot.
fn names_equal(a: &str, b: &str) -> bool {
    a.trim_end_matches('.')
        .eq_ignore_ascii_case(b.trim_end_matches('.'))
}

/// RFC 4034 §6.1 canonical name ordering: compare label sequences from the
/// rightmost label, each label as a lowercased octet string.
fn canonical_name_cmp(a: &str, b: &str) -> std::cmp::Ordering {
    let la: Vec<String> = a
        .trim_end_matches('.')
        .split('.')
        .filter(|s| !s.is_empty())
        .map(|s| s.to_lowercase())
        .rev()
        .collect();
    let lb: Vec<String> = b
        .trim_end_matches('.')
        .split('.')
        .filter(|s| !s.is_empty())
        .map(|s| s.to_lowercase())
        .rev()
        .collect();
    for (x, y) in la.iter().zip(lb.iter()) {
        // Label octets compare bytewise.
        match x.as_bytes().cmp(y.as_bytes()) {
            std::cmp::Ordering::Equal => {}
            other => return other,
        }
    }
    la.len().cmp(&lb.len())
}

/// Prove NXDOMAIN or NODATA from NSEC records.
fn prove_nsec(nsecs: &[&ResourceRecord], qname: &str, qtype: RecordType) -> Result<String, String> {
    // NODATA: an NSEC whose owner is exactly qname, with qtype absent from its
    // type bitmap (and no CNAME, which would have been followed).
    for rr in nsecs {
        if names_equal(&rr.name, qname) {
            if let RData::NSEC { type_bitmaps, .. } = &rr.rdata {
                if type_bitmaps.contains(&qtype) {
                    return Err(format!("NSEC at {} asserts {} exists", qname, qtype));
                }
                if type_bitmaps.contains(&RecordType::CNAME) {
                    return Err("NSEC indicates a CNAME that should have been followed".to_string());
                }
                return Ok(format!("NODATA proven: NSEC at {} lacks {}", qname, qtype));
            }
        }
    }

    // NXDOMAIN: an NSEC whose range (owner, next_domain] strictly covers qname.
    use std::cmp::Ordering;
    for rr in nsecs {
        let RData::NSEC { next_domain, .. } = &rr.rdata else {
            continue;
        };
        let owner_vs_q = canonical_name_cmp(&rr.name, qname);
        let q_vs_next = canonical_name_cmp(qname, next_domain);
        let owner_vs_next = canonical_name_cmp(&rr.name, next_domain);
        let covered = if owner_vs_next == Ordering::Less {
            owner_vs_q == Ordering::Less && q_vs_next == Ordering::Less
        } else {
            // Last NSEC wraps past the apex: covers anything after owner.
            owner_vs_q == Ordering::Less || q_vs_next == Ordering::Less
        };
        if covered {
            return Ok(format!(
                "NXDOMAIN proven: NSEC {} < {} < {}",
                rr.name, qname, next_domain
            ));
        }
    }
    Err("no NSEC covers the query name".to_string())
}

/// The SHA-1 NSEC3 hash of a name (RFC 5155 §5): H = SHA1(name || salt),
/// then H = SHA1(H || salt) `iterations` more times.
fn nsec3_hash(name: &str, iterations: u16, salt: &[u8]) -> Option<Vec<u8>> {
    let mut data = canonical_name(name).ok()?;
    data.extend_from_slice(salt);
    let mut hash = digest::digest(&digest::SHA1_FOR_LEGACY_USE_ONLY, &data)
        .as_ref()
        .to_vec();
    for _ in 0..iterations {
        let mut round = hash.clone();
        round.extend_from_slice(salt);
        hash = digest::digest(&digest::SHA1_FOR_LEGACY_USE_ONLY, &round)
            .as_ref()
            .to_vec();
    }
    Some(hash)
}

/// Decode a base32hex label (the NSEC3 owner's first label) to raw bytes.
fn base32hex_decode(label: &str) -> Option<Vec<u8>> {
    let mut bits = 0u32;
    let mut nbits = 0u32;
    let mut out = Vec::new();
    for c in label.bytes() {
        let v = match c {
            b'0'..=b'9' => c - b'0',
            b'A'..=b'V' => c - b'A' + 10,
            b'a'..=b'v' => c - b'a' + 10,
            _ => return None,
        } as u32;
        bits = (bits << 5) | v;
        nbits += 5;
        if nbits >= 8 {
            nbits -= 8;
            out.push((bits >> nbits) as u8);
        }
    }
    Some(out)
}

/// The owner hash of an NSEC3 record: its first label, base32hex-decoded.
fn nsec3_owner_hash(rr: &ResourceRecord) -> Option<Vec<u8>> {
    let first = rr.name.split('.').next()?;
    base32hex_decode(first)
}

/// Does this NSEC3 cover `hash` (owner_hash < hash < next_hash, circular)?
fn nsec3_covers(rr: &ResourceRecord, hash: &[u8]) -> bool {
    let (Some(owner), RData::NSEC3 { next_hashed, .. }) = (nsec3_owner_hash(rr), &rr.rdata) else {
        return false;
    };
    use std::cmp::Ordering;
    match owner.as_slice().cmp(next_hashed.as_slice()) {
        Ordering::Less => owner.as_slice() < hash && hash < next_hashed.as_slice(),
        // The zone's last NSEC3 wraps around the ordering.
        _ => hash > owner.as_slice() || hash < next_hashed.as_slice(),
    }
}

fn nsec3_matches(rr: &ResourceRecord, hash: &[u8]) -> bool {
    nsec3_owner_hash(rr).as_deref() == Some(hash)
}

/// Prove NXDOMAIN or NODATA from NSEC3 records (RFC 5155 §8.4–8.6).
fn prove_nsec3(
    nsec3s: &[&ResourceRecord],
    qname: &str,
    zone: &str,
    qtype: RecordType,
) -> Result<String, String> {
    let (iterations, salt) = match &nsec3s[0].rdata {
        RData::NSEC3 {
            iterations, salt, ..
        } => (*iterations, salt.clone()),
        _ => return Err("not an NSEC3 record".to_string()),
    };
    let hash = |name: &str| nsec3_hash(name, iterations, &salt);

    // NODATA: an NSEC3 whose owner hash equals H(qname), qtype absent.
    if let Some(qhash) = hash(qname) {
        for rr in nsec3s {
            if nsec3_matches(rr, &qhash) {
                if let RData::NSEC3 { type_bitmaps, .. } = &rr.rdata {
                    if type_bitmaps.contains(&qtype) {
                        return Err(format!("NSEC3 asserts {} exists", qtype));
                    }
                    return Ok(format!(
                        "NODATA proven: NSEC3 for {} lacks {}",
                        qname, qtype
                    ));
                }
            }
        }
    }

    // NXDOMAIN: closest-encloser proof.
    // 1. Find the closest encloser: the longest ancestor of qname whose hash
    //    matches an NSEC3 owner exactly.
    let labels: Vec<&str> = qname.trim_end_matches('.').split('.').collect();
    let zone_labels = zone
        .trim_end_matches('.')
        .split('.')
        .filter(|s| !s.is_empty())
        .count();
    let mut closest_encloser: Option<String> = None;
    let mut next_closer: Option<String> = None;
    // Ancestors from the full name up to (and including) the zone apex.
    for start in 0..=labels.len().saturating_sub(zone_labels) {
        let candidate_labels = &labels[start..];
        if candidate_labels.is_empty() {
            break;
        }
        let candidate = format!("{}.", candidate_labels.join("."));
        if let Some(chash) = hash(&candidate) {
            if nsec3s.iter().any(|rr| nsec3_matches(rr, &chash)) {
                closest_encloser = Some(candidate);
                if start > 0 {
                    next_closer = Some(format!("{}.", labels[start - 1..].join(".")));
                }
                break;
            }
        }
    }

    let Some(ce) = closest_encloser else {
        return Err("no NSEC3 matches any closest encloser".to_string());
    };
    let Some(nc) = next_closer else {
        // qname itself matched an NSEC3 owner but had the type — handled above;
        // reaching here means the name provably exists, so NXDOMAIN is a lie.
        return Err("closest encloser equals the query name".to_string());
    };

    // 2. An NSEC3 must cover the next-closer name's hash.
    let nc_hash = hash(&nc).ok_or("cannot hash next-closer name")?;
    let nc_covered = nsec3s.iter().find(|rr| nsec3_covers(rr, &nc_hash));
    let Some(nc_rr) = nc_covered else {
        return Err("no NSEC3 covers the next-closer name".to_string());
    };

    // 3. Wildcard *.<closest-encloser> must also be covered (or opt-out set on
    //    the next-closer NSEC3, RFC 5155 §8.6).
    let opt_out = matches!(&nc_rr.rdata, RData::NSEC3 { flags, .. } if flags & 0x01 != 0);
    let wildcard = format!("*.{}", ce);
    let wc_hash = hash(&wildcard).ok_or("cannot hash wildcard")?;
    let wc_covered = nsec3s
        .iter()
        .any(|rr| nsec3_covers(rr, &wc_hash) || nsec3_matches(rr, &wc_hash));
    let _ = qtype;

    if wc_covered || opt_out {
        Ok(format!(
            "NXDOMAIN proven: closest encloser {}, next-closer covered{}",
            ce,
            if opt_out {
                " (opt-out)"
            } else {
                ", wildcard covered"
            }
        ))
    } else {
        Err("wildcard existence not denied".to_string())
    }
}

