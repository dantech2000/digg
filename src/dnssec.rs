    #[test]
    fn tampered_nsec3_hash_is_not_proven() {
        // Flip a bit in the answer's authority NSEC3 next_hashed via raw
        // mutation: corrupt the wire then re-parse. A broken signature must
        // yield Bogus, never Proven.
        let mut wire = NSEC3_NXDOMAIN.to_vec();
        // Corrupt a byte well inside the authority section (past the header
        // and question) — lands in an NSEC3/RRSIG rdata region.
        let i = wire.len() - 40;
        wire[i] ^= 0xFF;
        let answer = match DnsMessage::parse(&wire) {
            Ok(m) => m,
            Err(_) => return, // corruption broke parsing entirely: also fine
        };
        let fetch = denial_fetch("verisign.com.", NSEC3_NXDOMAIN_DNSKEY);
        let outcome = validate_denial(
            &answer,
            "nope12345xyz.verisign.com",
            RecordType::A,
            FIXED_DENIAL_NOW,
            &fetch,
        )
        .unwrap();
        assert!(
            !matches!(outcome, DenialOutcome::Proven { .. }),
            "a tampered denial must not be proven SECURE"
        );
    }

