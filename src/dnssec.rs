    #[test]
    fn nsec3_hash_matches_rfc5155_shape() {
        // The hash is 20 bytes (SHA-1) regardless of iterations.
        let h = nsec3_hash("example.com.", 5, &[0xAA, 0xBB]).unwrap();
        assert_eq!(h.len(), 20);
        // Deterministic: same inputs, same output.
        assert_eq!(h, nsec3_hash("example.com.", 5, &[0xAA, 0xBB]).unwrap());
        // Iterations matter.
        assert_ne!(h, nsec3_hash("example.com.", 6, &[0xAA, 0xBB]).unwrap());
    }

