    fn full_message(wire: &[u8]) -> DnsMessage {
        DnsMessage::parse(wire).expect("fixture parses")
    }

    fn denial_fetch(
        dnskey_zone: &'static str,
        dnskey_wire: &'static [u8],
    ) -> impl Fn(&str, RecordType) -> Result<DnsMessage, DnsError> {
        move |name: &str, rtype: RecordType| {
            if rtype == RecordType::DNSKEY && name == dnskey_zone {
                DnsMessage::parse(dnskey_wire)
            } else {
                // Chain-to-root beyond the signing zone is not exercised here;
                // return an empty message so the chain walk degrades to a
                // clear error rather than touching the network.
                DnsMessage::parse(&[0u8; 12])
            }
        }
    }

    #[test]
    fn nsec_nodata_denial_is_proven() {
        let answer = full_message(NSEC_NODATA);
        let fetch = denial_fetch("nlnetlabs.nl.", NSEC_NODATA_DNSKEY);
        // HINFO (type 13) NODATA on nlnetlabs.nl.
        let outcome = validate_denial(
            &answer,
            "nlnetlabs.nl",
            RecordType::Unknown(13),
            FIXED_DENIAL_NOW,
            &fetch,
        )
        .unwrap();
        match outcome {
            DenialOutcome::Proven { denial_links, .. } => {
                assert!(denial_links.iter().all(|l| l.ok));
                assert!(denial_links
                    .iter()
                    .any(|l| l.detail.contains("NODATA proven")));
            }
            DenialOutcome::Bogus { reason, .. } => panic!("expected proven, got bogus: {}", reason),
            DenialOutcome::Unsigned(w) => panic!("expected proven, got unsigned: {}", w),
        }
    }

