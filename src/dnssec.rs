    fn wildcard_owner_reduces_to_rrsig_label_count() {
        assert_eq!(wildcard_owner("a.b.example.com.", 2), "*.example.com.");
        assert_eq!(wildcard_owner("example.com.", 2), "example.com.");
        assert_eq!(wildcard_owner("www.example.com.", 3), "www.example.com.");
        // Root has zero labels and must never be wildcard-adjusted.
        assert_eq!(wildcard_owner(".", 0), ".");
    }

    #[test]
    fn rsa_dnskey_der_encoding_is_wellformed() {
        // explen=1, exponent 65537 (0x010001), tiny "modulus".
        let key = [3u8, 0x01, 0x00, 0x01, 0xC9, 0x00, 0x01];
        let der = rsa_dnskey_to_der(&key).unwrap();
        assert_eq!(der[0], 0x30); // SEQUENCE
                                  // modulus INTEGER gets a leading zero (0xC9 high bit set)
        assert_eq!(&der[2..4], &[0x02, 0x04]);
        assert_eq!(&der[4..8], &[0x00, 0xC9, 0x00, 0x01]);
    }

