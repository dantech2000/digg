    fn answers(wire: &[u8]) -> Vec<ResourceRecord> {
        DnsMessage::parse(wire).expect("fixture parses").answers
    }

    fn keys(records: &[ResourceRecord]) -> Vec<&ResourceRecord> {
        records
            .iter()
            .filter(|rr| rr.rtype == RecordType::DNSKEY)
            .collect()
    }

    #[test]
    fn root_ksk_matches_builtin_trust_anchor() {
        let root = answers(ROOT_DNSKEY);
        let matched = keys(&root).iter().any(|key| {
            ROOT_ANCHORS.iter().any(|anchor| {
                key_tag(&key.raw_rdata) == anchor.key_tag
                    && ds_digest(".", key, 2).as_deref() == Some(anchor.digest)
            })
        });
        assert!(matched, "live root DNSKEY should match an IANA anchor");
    }

    #[test]
    fn real_ecdsa_rrsig_verifies_over_answer_rrset() {
        let recs = answers(CF_A);
        let rrset: Vec<&ResourceRecord> =
            recs.iter().filter(|r| r.rtype == RecordType::A).collect();
        let rrsig = recs
            .iter()
            .find(|r| matches!(&r.rdata, RData::RRSIG { type_covered, .. } if *type_covered == RecordType::A))
            .expect("fixture has RRSIG");
        let key_recs = answers(CF_DNSKEY);
        let dnskeys = keys(&key_recs);
        let tag = verify_rrsig(&rrset, rrsig, &dnskeys, FIXED_NOW).expect("signature verifies");
        assert!(tag > 0);
    }

    #[test]
    fn tampered_rdata_fails_verification() {
        let mut recs = answers(CF_A);
        // Flip one bit in an A record's rdata.
        if let Some(rr) = recs.iter_mut().find(|r| r.rtype == RecordType::A) {
            rr.raw_rdata[0] ^= 1;
        }
        let rrset: Vec<&ResourceRecord> =
            recs.iter().filter(|r| r.rtype == RecordType::A).collect();
        let rrsig = recs
            .iter()
            .find(|r| matches!(&r.rdata, RData::RRSIG { type_covered, .. } if *type_covered == RecordType::A))
            .unwrap();
        let key_recs = answers(CF_DNSKEY);
        assert!(verify_rrsig(&rrset, rrsig, &keys(&key_recs), FIXED_NOW).is_err());
    }

    #[test]
    fn signature_time_window_is_enforced() {
        let recs = answers(CF_A);
        let rrset: Vec<&ResourceRecord> =
            recs.iter().filter(|r| r.rtype == RecordType::A).collect();
        let rrsig = recs
            .iter()
            .find(|r| matches!(&r.rdata, RData::RRSIG { type_covered, .. } if *type_covered == RecordType::A))
            .unwrap();
        let key_recs = answers(CF_DNSKEY);
        let dnskeys = keys(&key_recs);
        let expired = verify_rrsig(&rrset, rrsig, &dnskeys, u32::MAX).unwrap_err();
        assert!(expired.contains("expired"), "{}", expired);
        let early = verify_rrsig(&rrset, rrsig, &dnskeys, 0).unwrap_err();
        assert!(early.contains("not yet valid"), "{}", early);
    }

    #[test]
    fn ds_digest_links_child_ksk_to_parent() {
        let ds_recs = answers(COM_DS);
        let key_recs = answers(CF_DNSKEY);
        let matched = ds_recs
            .iter()
            .filter_map(|rr| match &rr.rdata {
                RData::DS {
                    key_tag: tag,
                    digest_type,
                    digest,
                    ..
                } => Some((tag, digest_type, digest)),
                _ => None,
            })
            .any(|(tag, dt, digest)| {
                keys(&key_recs).iter().any(|key| {
                    key_tag(&key.raw_rdata) == *tag
                        && ds_digest("cloudflare.com.", key, *dt).as_deref()
                            == Some(digest.as_slice())
                })
            });
        assert!(matched, "cloudflare.com DS should match its KSK");
    }

    #[test]
    fn full_chain_validates_secure_from_fixtures() {
        let fetch = |name: &str, rtype: RecordType| -> Result<DnsMessage, DnsError> {
            let wire = match (name, rtype) {
                ("cloudflare.com.", RecordType::DNSKEY) => CF_DNSKEY,
                ("cloudflare.com.", RecordType::DS) => COM_DS,
                ("com.", RecordType::DNSKEY) => COM_DNSKEY,
                ("com.", RecordType::DS) => COM_DS_AT_ROOT,
                (".", RecordType::DNSKEY) => ROOT_DNSKEY,
                other => panic!("unexpected fetch: {:?}", other),
            };
            DnsMessage::parse(wire)
        };
        let answer = DnsMessage::parse(CF_A).unwrap();
        let report =
            validate_with(&answer, "cloudflare.com", RecordType::A, FIXED_NOW, &fetch).unwrap();
        for link in &report.links {
            assert!(link.ok, "link failed: {} {}", link.zone, link.detail);
        }
        assert_eq!(report.status, ChainStatus::Secure);
        // Chain touches all three zones.
        let zones: Vec<&str> = report.links.iter().map(|l| l.zone.as_str()).collect();
        assert!(zones.contains(&"cloudflare.com."));
        assert!(zones.contains(&"com."));
        assert!(zones.contains(&"."));
    }

    #[test]
    fn key_tag_matches_rrsig_reference() {
        // The fixture RRSIG names the key that signed it; our computed tag
        // over that DNSKEY's RDATA must agree.
        let recs = answers(CF_A);
        let sig_tag = recs
            .iter()
            .find_map(|r| match &r.rdata {
                RData::RRSIG {
                    key_tag,
                    type_covered,
                    ..
                } if *type_covered == RecordType::A => Some(*key_tag),
                _ => None,
            })
            .unwrap();
        let key_recs = answers(CF_DNSKEY);
        assert!(
            keys(&key_recs)
                .iter()
                .any(|k| key_tag(&k.raw_rdata) == sig_tag),
            "no DNSKEY matches RRSIG key tag {}",
            sig_tag
        );
    }

